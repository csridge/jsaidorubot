function mirrorPST(pst) {
  const mirrored = [];
  for (let rank = 0; rank < 8; rank++) {
    const row = pst.slice(rank * 8, (rank + 1) * 8);
    mirrored.unshift(...row);  // Add to front (flips vertically)
  }
  return mirrored;
}

